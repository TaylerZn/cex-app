  static Color hexToColor(String hex) {
    assert(RegExp(r'^#([0-9a-fA-F]{6})|([0-9a-fA-F]{8})$').hasMatch(hex),
        'hex color must be #rrggbb or #rrggbbaa');

    return Color(
      int.parse(hex.substring(1), radix: 16) +
          (hex.length == 7 ? 0xff000000 : 0x00000000),
    );
  }

  static Color hexToRGBO(String code, [double opacity = 1]) {
    final int r = int.parse(code.substring(1, 3), radix: 16);
    final int g = int.parse(code.substring(3, 5), radix: 16);
    final int b = int.parse(code.substring(5, 7), radix: 16);
    return Color.fromRGBO(r, g, b, opacity);
  }

  static Color upDownColor(num value) {
    if (value > 0) {
      return upColor;
    } else if (value < 0) {
      return downColor;
    } else {
      return color999999;
    }
  }

  static MaterialColor createMaterialColor(Color color) {
    List strengths = <double>[.05];
    Map<int, Color> swatch = {};
    final int r = color.red, g = color.green, b = color.blue;

    for (int i = 1; i < 10; i++) {
      strengths.add(0.1 * i);
    }
    strengths.forEach((strength) {
      final double ds = 0.5 - strength;
      swatch[(strength * 1000).round()] = Color.fromRGBO(
        r + ((ds < 0 ? r : (255 - r)) * ds).round(),
        g + ((ds < 0 ? g : (255 - g)) * ds).round(),
        b + ((ds < 0 ? b : (255 - b)) * ds).round(),
        1,
      );
    });
    return MaterialColor(color.value, swatch);
  }
